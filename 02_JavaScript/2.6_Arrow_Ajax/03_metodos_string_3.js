// 3. Métodos de String (3/3)
// Exemplos de métodos: replace, split, trim, includes
const texto = "  Olá, mundo!  ";
console.log("replace:", texto.replace("mundo", "JavaScript"));
console.log("split:", texto.split(","));
console.log("trim:", texto.trim());
console.log("includes 'Olá':", texto.includes("Olá"));
