// 6. Métodos de Arrays (2/4)
// Exemplos: shift, unshift, join
const frutas = ["maçã", "banana", "uva"];
frutas.shift(); // Remove o primeiro
console.log("Após shift:", frutas);
frutas.unshift("laranja"); // Adiciona no início
console.log("Após unshift:", frutas);
console.log("join:", frutas.join(" - "));
