// 4. Métodos de Numbers
// Exemplos: toFixed, parseInt, parseFloat, isNaN
const numero = 123.456;
console.log("toFixed(1):", numero.toFixed(1)); // "123.5"
console.log("parseInt:", parseInt("10.5")); // 10
console.log("parseFloat:", parseFloat("10.5")); // 10.5
console.log("isNaN('abc'):", isNaN("abc")); // true
