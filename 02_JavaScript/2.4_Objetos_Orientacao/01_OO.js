// 01 - O que é OO?
// Orientação a Objetos (OO) é um paradigma de programação que organiza o código em objetos.
// Objetos possuem propriedades (dados) e métodos (funções), facilitando a modelagem do mundo real.

const carro = {
  marca: 'Toyota',
  modelo: 'Corolla',
  ligar: function() {
    console.log('Carro ligado!');
  }
};

carro.ligar(); // Carro ligado!
