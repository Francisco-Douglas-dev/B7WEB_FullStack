// Aula: Switch
// Estrutura para múltiplas condições

let cor = 'azul';
switch (cor) {
    case 'vermelho':
        console.log('Cor vermelha');
        break;
    case 'azul':
        console.log('Cor azul');
        break;
    case 'verde':
        console.log('Cor verde');
        break;
    default:
        console.log('Outra cor');
}