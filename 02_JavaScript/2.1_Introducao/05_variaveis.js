// Aula: Variáveis
// Variáveis armazenam valores que podem ser usados e alterados no código.
// Podemos criar variáveis usando let, const ou var.

let nome = 'Maria'; // variável do tipo string
var idade = 25;     // variável do tipo número
const PI = 3.14;    // constante, valor não pode ser alterado

console.log(nome, idade, PI);