// Aula: Tipos de dados
// JavaScript possui vários tipos de dados: string, number, boolean, null, undefined, object, array

let texto = 'Olá'; // string
let numero = 10;   // number
let ativo = true;  // boolean
let nada = null;   // null
let indefinido;    // undefined
let pessoa = { nome: 'Ana', idade: 20 }; // object
let lista = [1, 2, 3]; // array

console.log(typeof texto, typeof numero, typeof ativo, typeof nada, typeof indefinido, typeof pessoa, typeof lista);